#include "libft.h"

void	ft_striteri(char *s, void (*f) (unsigned int, char *))
{
	int		i;
	int		j;

	j = 0;
	i = 0;
	if (s == NULL || f == NULL)
		return ;
	while (s[i])
	{
		f(j, &s[j]);
		i++;
		j++;
	}
}
