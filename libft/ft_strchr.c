#include "libft.h"

char	*ft_strchr(char *s, int c)
{
	int i;

	i = 0;
	while (s[i] != (char)c)
	{
		if (s[i] == '\0' && (char)c != '\0')
			return (0);
		i++;
	}
	return (s + i);
}
