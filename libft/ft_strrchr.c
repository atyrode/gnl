#include "libft.h"

char	*ft_strrchr(char *s, int c)
{
	int i;

	i = ft_strlen(s);
	if (i == 0)
		return (0);
	while (i != 0 && s[i] != (char)c)
	{
		i--;
	}
	if (i == 0 && s[0] != (char)c)
		return (0);
	return (s + i);
}
