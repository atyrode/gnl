#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*j;

	i = 0;
	if (!(j = malloc(sizeof(char) * (ft_strlen(s1) + 1))))
		return (0);
	while (s1[i])
	{
		j[i] = s1[i];
		i++;
	}
	j[i] = '\0';
	return (j);
}
