#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char *new;

	if (s1 == NULL || s2 == NULL)
		return (0);
	if (!(new = ft_strnew(ft_strlen(s1) + ft_strlen(s2))))
		return (0);
	ft_strcpy(new, s1);
	return (ft_strcat(new, s2));
}
